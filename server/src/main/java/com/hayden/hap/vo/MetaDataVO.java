package com.hayden.hap.vo;

public class MetaDataVO {

	private String metaDataType;
	private String metaDataCode;
	private String metaDataName;
	private String metaDataLinkType;
	public String getMetaDataType() {
		return metaDataType;
	}
	public void setMetaDataType(String metaDataType) {
		this.metaDataType = metaDataType;
	}
	public String getMetaDataCode() {
		return metaDataCode;
	}
	public void setMetaDataCode(String metaDataCode) {
		this.metaDataCode = metaDataCode;
	}
	public String getMetaDataName() {
		return metaDataName;
	}
	public void setMetaDataName(String metaDataName) {
		this.metaDataName = metaDataName;
	}
	public String getMetaDataLinkType() {
		return metaDataLinkType;
	}
	public void setMetaDataLinkType(String metaDataLinkType) {
		this.metaDataLinkType = metaDataLinkType;
	}
	
}
